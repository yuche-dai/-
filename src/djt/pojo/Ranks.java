package djt.pojo;

public class Ranks {
    private int id;
    private String name;
    private String konlon;
    private String powers;
    private String ranking;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getKonlon() {
        return konlon;
    }

    public void setKonlon(String konlon) {
        this.konlon = konlon;
    }

    public String getPowers() {
        return powers;
    }

    public void setPowers(String powers) {
        this.powers = powers;
    }

    public String getRanking() {
        return ranking;
    }

    public void setRanking(String ranking) {
        this.ranking = ranking;
    }
}
